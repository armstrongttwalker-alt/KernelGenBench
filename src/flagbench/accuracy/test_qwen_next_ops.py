def native_per_token_group_quant_fp8(x, group_size, eps=1e-10, dtype=None):
    if dtype is None:
        dtype = flag_gems.SUPPORTED_FP8_DTYPE
    assert x.shape[-1] % group_size == 0, 'the last dimension of `x` cannot be divisible by `group_size`'
    assert x.is_contiguous(), '`x` is not contiguous'
    finfo = torch.finfo(dtype)
    fp8_min = finfo.min
    fp8_max = finfo.max
    x_ = x.reshape(x.numel() // group_size, group_size)
    amax = x_.abs().max(dim=-1, keepdim=True)[0].clamp(min=eps).to(torch.float32)
    x_s = amax / fp8_max
    x_q = (x_ / x_s).clamp(min=fp8_min, max=fp8_max).to(dtype)
    x_q = x_q.reshape(x.shape)
    x_s = x_s.reshape(x.shape[:-1] + (x.shape[-1] // group_size,))
    return (x_q, x_s)

def ref_paged_attn(query: torch.Tensor, key_cache: torch.Tensor, value_cache: torch.Tensor, query_lens: List[int], kv_lens: List[int], block_tables: torch.Tensor, scale: float, attn_bias: torch.Tensor=None, sliding_window: Optional[int]=None, soft_cap: Optional[float]=None) -> torch.Tensor:
    num_seqs = len(query_lens)
    block_tables = block_tables.cpu().numpy()
    _, block_size, num_kv_heads, head_size = key_cache.shape
    outputs: List[torch.Tensor] = []
    start_idx = 0
    for i in range(num_seqs):
        query_len = query_lens[i]
        kv_len = kv_lens[i]
        q = query[start_idx:start_idx + query_len].clone()
        q *= scale
        num_kv_blocks = (kv_len + block_size - 1) // block_size
        block_indices = block_tables[i, :num_kv_blocks]
        k = key_cache[block_indices].view(-1, num_kv_heads, head_size)
        k = k[:kv_len]
        v = value_cache[block_indices].view(-1, num_kv_heads, head_size)
        v = v[:kv_len]
        if q.shape[1] != k.shape[1]:
            k = torch.repeat_interleave(k, q.shape[1] // k.shape[1], dim=1)
            v = torch.repeat_interleave(v, q.shape[1] // v.shape[1], dim=1)
        attn = torch.einsum('qhd,khd->hqk', q, k)
        empty_mask = torch.ones(query_len, kv_len)
        mask = torch.triu(empty_mask, diagonal=kv_len - query_len + 1).bool()
        if sliding_window is not None:
            sliding_window_mask = torch.triu(empty_mask, diagonal=kv_len - (query_len + sliding_window) + 1).bool().logical_not()
            mask |= sliding_window_mask
        if soft_cap is not None:
            attn = soft_cap * torch.tanh(attn / soft_cap)
        attn.masked_fill_(mask, float('-inf'))
        if attn_bias is not None:
            attn = attn + attn_bias[i, :, :query_len, :kv_len]
        attn = torch.softmax(attn, dim=-1).to(v.dtype)
        out = torch.einsum('hqk,khd->qhd', attn, v)
        outputs.append(out)
        start_idx += query_len
    return torch.cat(outputs, dim=0)

def replace_zeros(inp):
    return torch.where(inp == 0, 1, inp)

def rotate_half(x):
    """Rotates half the hidden dims of the input."""
    x1 = x[..., :x.shape[-1] // 2]
    x2 = x[..., x.shape[-1] // 2:]
    return torch.cat((-x2, x1), dim=-1)

def rotate_interleave(x):
    """Rotates interleave the hidden dims of the input."""
    x1 = x[..., ::2]
    x2 = x[..., 1::2]
    return torch.stack((-x2, x1), dim=-1).flatten(-2)

def topk_softmax_torch_reference(gating_output: torch.Tensor, topk: int):
    probs = torch.softmax(gating_output, dim=-1)
    topk_values, topk_indices = torch.topk(probs, k=topk, dim=-1, largest=True, sorted=True)
    num_tokens = gating_output.shape[0]
    source_rows = torch.arange(topk, device=gating_output.device).view(1, -1) * num_tokens + torch.arange(num_tokens, device=gating_output.device).view(-1, 1)
    return (topk_values, topk_indices, source_rows)

def torch_apply_rotary_pos_emb(q, k, cos, sin, position_ids: Optional[torch.Tensor]=None, rotary_interleaved: bool=False):
    q = q.float()
    k = k.float()
    if position_ids is None:
        cos = cos[None, :q.size(-3), None, :]
        sin = sin[None, :q.size(-3), None, :]
    else:
        cos = cos[position_ids].unsqueeze(-2)
        sin = sin[position_ids].unsqueeze(-2)
    if rotary_interleaved:
        cos = torch.repeat_interleave(cos, 2, dim=-1)
        sin = torch.repeat_interleave(sin, 2, dim=-1)
        rotate_fn = rotate_interleave
    else:
        cos = torch.cat([cos, cos], dim=-1)
        sin = torch.cat([sin, sin], dim=-1)
        rotate_fn = rotate_half
    q_embed = q * cos + rotate_fn(q) * sin
    k_embed = k * cos + rotate_fn(k) * sin
    return (q_embed, k_embed)

def torch_flash_fwd(q, k, v, scale, is_causal, dropout_p=0, return_debug_mask=False, **extra_kwargs):
    q = q.transpose(1, 2)
    k = k.transpose(1, 2)
    v = v.transpose(1, 2)
    out, lse, seed, offset, debug_softmax = torch.ops.aten._flash_attention_forward(q, k, v, None, None, q.shape[-3], k.shape[-3], dropout_p, is_causal, return_debug_mask, scale=scale, **extra_kwargs)
    return (out, lse, seed, offset, debug_softmax)

def torch_sdpa(q, k, v, scale, is_causal, enable_gqa=False):
    if torch.__version__ < '2.5':
        torch_result = torch.nn.functional.scaled_dot_product_attention(q, k, v, attn_mask=None, scale=scale, is_causal=is_causal)
    else:
        if flag_gems.vendor_name == 'iluvatar' and TO_CPU:
            from torch.nn.attention import SDPBackend, sdpa_kernel
            ctx = sdpa_kernel(backends=[SDPBackend.MATH])
        else:
            from contextlib import nullcontext
            ctx = nullcontext()
        with ctx:
            torch_result = torch.nn.functional.scaled_dot_product_attention(q, k, v, attn_mask=None, scale=scale, is_causal=is_causal, enable_gqa=enable_gqa)
    return torch_result

